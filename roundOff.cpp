#include<bits/stdc++.h>
#include<iostream>
using namespace std;

int main(){
    float val1,val2;
    val1=1.234;
    val2=6.564;

    cout<< "floor is: "<<floor(val1) <<endl;
    cout<< "ceil is: " <<ceil(val1) <<endl;
    cout<< "floor is: " <<floor(val2) <<endl;
    cout<< "ceil is: " <<ceil(val2) <<endl;
    cout<< "round off value is: " <<round(val2) <<endl;
    return 0;
}

